export const componentRegistry = componentDefinitions.map(
  ([slug, name, section, group, category, description, composedFrom]) => ({
    slug,
    name,
    section,
    group,
    category,
    package: COMPONENT_PACKAGE,
    sourcePath: `packages/ui/src/components/${slug}.tsx`,
    catalogPath: `/${section}/${slug}/`,
    description,
    ...(composedFrom ? { composedFrom } : {}),
  }),
);

export function getComponentEntriesBySection(section) {
  return componentRegistry.filter((component) => component.section === section);
}

export function getComponentCountBySection(section) {
  return getComponentEntriesBySection(section).length;
}

export function groupComponentEntries(entries) {
  return entries.reduce((groups, component) => {
    let group = groups.find((candidate) => candidate.label === component.group);
    if (!group) {
      group = { label: component.group, components: [] };
      groups.push(group);
    }
    group.components.push(component);
    return groups;
  }, []);
}
